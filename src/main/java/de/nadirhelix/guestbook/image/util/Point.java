package de.nadirhelix.guestbook.image.util;

/**
 * Helper class representing a two-dimensional Point.
 * 
 * @author Phil
 */
public class Point {

	private final float x;
	
	private final float y;
	
	public Point(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
}
